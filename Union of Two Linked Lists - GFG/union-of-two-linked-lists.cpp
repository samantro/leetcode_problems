class Solution
{
    public:
    struct Node* makeUnion(struct Node* head1, struct Node* head2)
    {
        // code here
        set<int> s;
        struct Node *l1=head1,*l2=head2,*l3=NULL,*head=NULL;
        while(l1){
            s.insert(l1->data);
            l1=l1->next;
        }
        while(l2){
            s.insert(l2->data);
            l2=l2->next;
        }
        for(auto i:s){
            struct Node *t = new Node(i);
                if(!head){
                    l3=t;
                    head=t;
                }else{
                    l3->next=t;
                    l3=l3->next;
                }
        }
        return head;
    }
};


