class Solution {
  public:
    long long maxDiamonds(int A[], int N, int K) {
        // code here
        priority_queue<int> p;
        for(int i=0;i<N;i++) p.push(A[i]);
        long long res = 0;
        while(!p.empty() && K--){
            res+=p.top();
            if(p.top()>1) p.push(p.top()/2);
            p.pop();
        }
        return res;
    }
};

